# print() = comando de saida
print ("hello world!")

# Quando quiser guardar uma String! (frase)
nome = "Rafael Tanaka"
# Quando quiser guardae um numero inteiro
idade = 18

# Exibir o meu nome (que está dentro da variavel nome)
print (nome)

# Quando quiser exibir a frase "Minha idade é " completando com o conteudo da variavel idade
print ("Minha idade é "+str(idade))
# Ou ----- f = formatação ---- se quiser pular linha (\n ou +n)
print ( f"Minha idade é {idade}")
# Ou
print ("Minha idade é {}".format(idade))

# Quando quiser exibir "Meu nome é ... e tenho.." trocando pelas variaveis nome e idade
print("Meu nome é {} e tenho {} anos".format(nome,idade))

